inline void matmul_block(int *a, int *b, int *c)
{
    for (int i = 0; i < B; i++) {
        for (int j = 0; j < B; j++) {
            int cij = c[i * N + j];
            for (int k = 0; k < B; k++)
                cij += a[i * N + k] * b[k * N + j];
            c[i * N + j] = cij;
        }
    }
}

