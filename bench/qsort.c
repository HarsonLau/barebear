
static void qsort(int a[], int l, int r)
{
    int i, j, p, tmp;
    if (l + 1 >= r)
        return;

    p = a[(l + r) / 2];

    i = l;
    j = r - 1;
    while (1) {
        while (a[i] < p)
            i = i + 1;
        while (p < a[j])
            j = j - 1;
        if (i >= j)
            break;
        else {
            tmp = a[i];
            a[i] = a[j];
            a[j] = tmp;
            i = i + 1;
            j = j - 1;
        }
    }

    qsort(a, l, i);
    qsort(a, i, r);
}

